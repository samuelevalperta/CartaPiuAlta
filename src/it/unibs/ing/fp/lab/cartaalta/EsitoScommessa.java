package it.unibs.ing.fp.lab.cartaalta;

/**
 * Enum class EsitoScommessa representing the possible outcomes of a bet.
 * It can be one of the following values:
 * - VINTA: The bet is won.
 * - PERSA: The bet is lost.
 * - PATTA: The bet is a draw.
 */
public enum EsitoScommessa {
    VINTA, PERSA, PATTA
}
